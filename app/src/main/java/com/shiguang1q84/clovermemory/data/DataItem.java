package com.shiguang1q84.clovermemory.data;

public class DataItem{


    String head;
    String context;



    public DataItem(String head, String context){
        this.head = head;
        this.context = context;
    }

    public String getHead() {
        return head;
    }

    public String getContext() {
        return context;
    }

}
